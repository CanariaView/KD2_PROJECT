var chart = anychart.area();

var dataSet = anychart.data.set(getTotalData());	

chart.baseline(0);

var marker = chart.lineMarker();
marker.value(0);
marker.stroke({
    thickness: 1,
    dash: "4 4"
});

chart.textMarker(0, {
    value: 0,
    text: '',

    fontSize: 18,
    anchor: 'right-top',
    align: 'right'
});

chart.animation(true);

chart.padding([10, 20, 5, 20]);

chart.crosshair().enabled(true).yLabel(false).yStroke(null);

// set scale's ticks interval
chart.xScale().ticks().interval(12);

chart.tooltip().positionMode('point');

chart.yAxis().title('value');
chart.xAxis().labels().padding(5);

// create area series
var series = chart.splineArea(dataSet);

// set positive fill and stroke
series.fill(['#FE433C 0', '#FE433C 1'], 90);
series.stroke('#FE433C');

// set negative fill and stroke
series.negativeFill(['#3C50B1 1', '#3C50B1 0'], 90);
series.negativeStroke('#3C50B1');

// set container id for the chart
chart.container('plotContainer');

// initiate chart drawing
chart.draw();

let totalTable = document.getElementsByClassName("trendTable")[0];
let demandTable = document.getElementsByClassName("trendTable")[1];
let supplyTable = document.getElementsByClassName("trendTable")[2];

let plotTitle = document.getElementById("plotTitle");

// 수요
var button1 = document.getElementById("demand");

button1.addEventListener("click", function() {

	chart.dispose();
	
	chart = anychart.line();
		
	dataSet = anychart.data.set(getDemandData());	
	
	chart = anychart.column();

	firstSeriesData = dataSet.mapAs({ x: 0, value: 1 });
	
	secondSeriesData = dataSet.mapAs({ x: 0, value: 2 });
	
	chart.palette(["#eb472f", "#990000"]);
	
	chart.animation(true);
	
	chart.padding([10, 20, 5, 20]);
	
	chart.crosshair().enabled(true).yLabel(false).yStroke(null);
	
	chart.tooltip().positionMode('point');
	
	chart.yAxis().title('value');
	chart.xAxis().labels().padding(5);
	
	firstSeries = chart.line(firstSeriesData);
	firstSeries.name('Predict');
	firstSeries.hovered().markers().enabled(true).type('circle').size(4);
	firstSeries
		.tooltip()
		.position('right')
		.anchor('left-center')
		.offsetX(5)
		.offsetY(5);
	
	secondSeries = chart.line(secondSeriesData);
	secondSeries.name('Actual');
	secondSeries.hovered().markers().enabled(true).type('circle').size(4);
	secondSeries
		.tooltip()
		.position('right')
		.anchor('left-center')
		.offsetX(5)
		.offsetY(5);
	
	chart.legend().enabled(true).fontSize(13).margin([0, 0, 0, 120]);
	
	chart.container('plotContainer');
	chart.draw();
	
	totalTable.setAttribute('style', 'text-align: center; font-family:kotrag; font-weight:bold; display:none;');
	demandTable.setAttribute('style', 'text-align: center; font-family:kotrag; font-weight:bold; display:inline-block; border:none;');
	supplyTable.setAttribute('style', 'text-align: center; font-family:kotrag; font-weight:bold; display:none;');
	
	plotTitle.innerText = "World Copper Demand Predict Vs. Actual (2001.01 ~ 2023.09)";
	
});

